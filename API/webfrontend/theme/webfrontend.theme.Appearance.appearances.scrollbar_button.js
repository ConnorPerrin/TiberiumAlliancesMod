function style(n) {
    var t = ny;
    return t += n.left ? ko : n.right ? cs : n.up ? vo : cc, n.left || n.right ? {
        height: 16,
        icon: t,
        padding: [0, 0, 2, 2],
        width: 14
    } : {
        height: 14,
        icon: t,
        paddingBottom: 2,
        paddingLeft: 1,
        paddingTop: 0,
        width: 16
    }
}