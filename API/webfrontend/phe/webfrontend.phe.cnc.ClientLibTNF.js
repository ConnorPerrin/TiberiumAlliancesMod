function translateParameter(n, t) {
    var r = qx.core.Init.getApplication(),
        f = [],
        u;
    for (f.push(n), u = 0; u < t.length; u++) f.push(t[u]);
    switch (r.tr(n).toString()) {
        case r.tr(as).toString():
        case r.tr(ts).toString():
        case r.tr(fs).toString():
        case r.tr(cs).toString():
        case r.tr(ss).toString():
        case r.tr(us).toString():
        case r.tr(os).toString():
        case r.tr(po).toString():
        case r.tr(bo).toString():
        case r.tr(ns).toString():
        case r.tr(vs).toString():
        case r.tr(i).toString():
        case r.tr(is).toString():
        case r.tr(wo).toString():
            return r.tr.apply(r, f)
    }
    return uo + n + yf
}